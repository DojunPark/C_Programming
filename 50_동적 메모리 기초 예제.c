#include <stdio.h>
#include <stdlib.h>

int main(void)
{
	int *pi;  //pointer integer의 약자
	pi = (int *)malloc(sizeof(int)); // malloc = 메모리를 할당해라 
	if(pi == NULL) 
	{
		printf("동적메모리 할당에 실패하였습니다.\n");
		exit(1);
	}
	*pi = 100;
	printf("%d\n", *pi);
	// 동적메모리를 사용한 후에는 무조건 해당 메모리를 반납해야함 
	free(pi);
	
	return 0;
}
